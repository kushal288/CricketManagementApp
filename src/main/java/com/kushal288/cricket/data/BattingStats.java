package com.kushal288.cricket.data;

public class BattingStats
{

	private Integer innings;
	private Integer notOuts;
	private Integer runsScored;
	private Integer fours;
	private Integer sixes;
	private Integer halfCentury;
	private Integer centuries;
	private Integer ballsFaced;
	private Double battingAverage;
	private Double battingStrikeRate;

	public Integer getInnings()
	{
		return innings;
	}

	public void setInnings(Integer innings)
	{
		this.innings = innings;
	}

	public Integer getNotOuts()
	{
		return notOuts;
	}

	public void setNotOuts(Integer notOuts)
	{
		this.notOuts = notOuts;
	}

	public Integer getRunsScored()
	{
		return runsScored;
	}

	public void setRunsScored(Integer runsScored)
	{
		this.runsScored = runsScored;
	}

	public Integer getFours()
	{
		return fours;
	}

	public void setFours(Integer fours)
	{
		this.fours = fours;
	}

	public Integer getSixes()
	{
		return sixes;
	}

	public void setSixes(Integer sixes)
	{
		this.sixes = sixes;
	}

	public Integer getHalfCentury()
	{
		return halfCentury;
	}

	public void setHalfCentury(Integer halfCentury)
	{
		this.halfCentury = halfCentury;
	}

	public Integer getCenturies()
	{
		return centuries;
	}

	public void setCenturies(Integer centuries)
	{
		this.centuries = centuries;
	}

	public Integer getBallsFaced()
	{
		return ballsFaced;
	}

	public void setBallsFaced(Integer ballsFaced)
	{
		this.ballsFaced = ballsFaced;
	}

	public Double getBattingAverage()
	{
		return battingAverage;
	}

	public void setBattingAverage(Double battingAverage)
	{
		this.battingAverage = battingAverage;
	}

	public Double getBattingStrikeRate()
	{
		return battingStrikeRate;
	}

	public void setBattingStrikeRate(Double battingStrikeRate)
	{
		this.battingStrikeRate = battingStrikeRate;
	}

}
